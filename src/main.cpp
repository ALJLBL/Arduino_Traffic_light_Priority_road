#include <Arduino.h>
#include <lcd.h>
#include <can.h>


//===========================================================//
//========================変数宣言============================//
//===========================================================//


void setup() {
  // CAN初期化
  canInit();

  // LCD初期化
  lcd_init();
}

void loop() {
  unsigned data = 0;
  can_send(0x100, data);
}
